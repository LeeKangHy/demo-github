

import sys
from pathlib import Path
from cryptography import x509
from cryptography.hazmat.backends import default_backend

# Đường dẫn chứng chỉ (bạn có thể đổi lại nếu cần)
cert_path = Path(r"D:\Crypto\Certainly Not\2048b-rsa-example-cert.der")

# Đọc file DER
with open(cert_path, "rb") as f:
    data = f.read()

# Parse chứng chỉ X.509 (định dạng DER)
cert = x509.load_der_x509_certificate(data, default_backend())

# Lấy khóa công khai RSA
public_key = cert.public_key()
public_numbers = public_key.public_numbers()

# Lấy modulus (n) và exponent (e)
n = public_numbers.n
e = public_numbers.e

# In ra modulus ở dạng decimal
print(n)

